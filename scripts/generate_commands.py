def write_detail(entry: dict) -> None:
    detail = {
        "name": entry["name"],
        "description": entry["description"],
        "synopsis": entry["synopsis"],
        "tags": entry["tags"],
        "examples": entry["examples"],
        "options": entry["options"],
        "related": entry["related"],
    }
    if entry.get("notes"):
        detail["notes"] = entry["notes"]
    if entry.get("distroNotes"):
        detail["distroNotes"] = entry["distroNotes"]
    if entry.get("distroVariants"):
        detail["distroVariants"] = entry["distroVariants"]

    path = CMD_DIR / f"{entry['name']}.json"
    path.write_text(json.dumps(detail, indent=2) + "\n", encoding="utf-8")


def write_index(entries: list[dict]) -> None:
    index = {
        "version": 1,
        "commands": [
            {
                "name": e["name"],
                "summary": e["summary"],
                "example": e.get("example"),
                "basic": bool(e.get("basic")),
                "tags": e["tags"],
                "detailFile": f"{e['name']}.json",
                "distros": e["distros"],
                "aliases": e["aliases"],
            }
            for e in entries
        ],
    }
    (DATA / "commands.json").write_text(json.dumps(index, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    CMD_DIR.mkdir(parents=True, exist_ok=True)
    for old in CMD_DIR.glob("*.json"):
        old.unlink()

    names = [e["name"] for e in COMMANDS]
    if len(names) != len(set(names)):
        dupes = {n for n in names if names.count(n) > 1}
        raise SystemExit(f"Duplicate command names: {dupes}")

    entries = [enrich(dict(e)) for e in COMMANDS]
    for entry in entries:
        write_detail(entry)
    write_index(entries)

    basic_n = sum(1 for e in entries if e.get("basic"))
    package_n = sum(1 for e in entries if "package" in e.get("tags", []))
    print(f"Wrote {len(entries)} commands ({basic_n} basic, {package_n} package) to {CMD_DIR}")
    print(f"Index: {DATA / 'commands.json'}")


if __name__ == "__main__":
    main()
