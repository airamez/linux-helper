def cmd(
    name: str,
    summary: str,
    tags: list[str],
    description: str,
    synopsis: str,
    examples: list[tuple[str, str]],
    options: list[tuple[str, str]] | None = None,
    related: list[str] | None = None,
    notes: str | None = None,
    aliases: list[str] | None = None,
    distros: list[str] | None = None,
    distro_notes: dict[str, str] | None = None,
    distro_variants: dict[str, str] | None = None,
) -> dict:
    return {
        "name": name,
        "summary": summary,
        "tags": tags,
        "description": description,
        "synopsis": synopsis,
        "examples": [{"command": c, "description": d} for c, d in examples],
        "options": [{"flag": f, "description": d} for f, d in (options or [])],
        "related": related or [],
        "notes": notes,
        "aliases": aliases or [],
        "distros": distros or ["all"],
        "distroNotes": distro_notes,
        "distroVariants": distro_variants,
    }


def pick_example(entry: dict) -> str | None:
    name = entry["name"]
    if name in PRIMARY_EXAMPLE:
        return PRIMARY_EXAMPLE[name]
    examples = entry.get("examples") or []
    for ex in examples:
        cmd_text = (ex.get("command") or "").strip()
        if cmd_text and cmd_text != name:
            return cmd_text
    if examples:
        return (examples[0].get("command") or "").strip() or None
    return None


def enrich(entry: dict) -> dict:
    """Mark basic + attach primary example used on compact lists."""
    is_package = "package" in (entry.get("tags") or [])
    entry["basic"] = (entry["name"] in BASIC) and not is_package
    entry["example"] = pick_example(entry)
    return entry


